#  комментарий к коду
#  Run - режим запуска
#  Debug - режим отладки

#  input - ввод
#  = - оператор присвоения. Правая часть высчитывается, после чего записывается в левую часть
#  поэтому в левой части всегда ожидается переменная
name = input('Как вас зовут? ')
print(name)
print('Hello', name)
print(f"Hello, {name}!")

name = 'Another name'
print('Hello', name)
print(f"Hello, {name}!")

#  динамическая типизация
name = 5
print('Hello', name)
print(f"Hello, {name}!")

#  integer (int) - тип данных, целые числа
x = int()  # значение по умолчанию - 0
print(x)
x = 0
print(x)

